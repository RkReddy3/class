package Corejava;

public class Concatination {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		String c="Ram";
		String d="Krishna";
		double e=10.12;
		double f=21.12;
		 
		char g='@';
		char h='$';
		
		boolean i=true;
		boolean j=false;
		System.out.println(a+b);
		
		System.out.println(a+c+b+i);
		System.out.println(c+a);
		System.out.println(e+f);
		System.out.print(c);
		System.out.println(d);

	}

}
